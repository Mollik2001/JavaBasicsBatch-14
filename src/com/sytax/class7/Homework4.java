package com.sytax.class7;

public class Homework4 {

	public static void main(String[] args) {
	
		/*
		 * Print odd numbers between 20 and 50 (2 ways)
		 */
		int x = 21;
		while (x <= 50) {
			if (x % 2 == 1) {
				System.out.print(x + " ");
			}
			x++;
			
		}
		System.out.println(" ");
		int y = 21;
		while (y <= 50) {
			System.out.print(y + "  ");
			y += 2;
	}
}


  
}


  
 
